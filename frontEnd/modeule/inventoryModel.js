export function InventoryModel(size, qty, maxQty, colour, status, item, itemImage, date, totalValue, totalQty) {
    this.size = size;
    this.qty = qty;
    this.maxQty = maxQty;
    this.colour = colour;
    this.status = status;
    this.item = item;
    this.itemImage = itemImage;
    this.date = date;
    this.totalValue = totalValue;
    this.totalQty = totalQty;
}